package com.gordonmzizi;

public class Mathutils {

    public int add(int a, int b){
        return a + b;
    }
    public int substract(int a, int b){

        return a - b;
    }
    public double divide(int a, int b){
        return a / b;
    }
    public double multiply(int a, int b){
        return a * b;
    }
}
